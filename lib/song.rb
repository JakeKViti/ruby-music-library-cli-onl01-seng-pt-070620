class Song
  attr_accessor :name, :artist, :song

  @@all = []

  def initialize(name)
    @name = name
    save
  end

  def save
    @@all << self
  end

  def self.all
    @@all
  end

  def self.destroy_all
    @@all = []
  end

  def self.create(song)
    newSong = Song.new(song)
    newSong
  end

end
   
